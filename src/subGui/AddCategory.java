    private void loadSubCategoryTable() {
        
        String text = jTextField1.getText();
        
        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `sub_category` WHERE `sc_name` LIKE '%" + text + "%' ORDER BY `sub_category`.`sc_id` ASC");
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector v = new Vector();
                v.add(resultSet.getString("sc_id"));
                v.add(resultSet.getString("sc_name"));
                model.addRow(v);
                jTable1.setModel(model);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
